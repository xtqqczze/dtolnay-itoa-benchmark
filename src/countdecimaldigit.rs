pub fn count_decimal_digit_64(n: u64) -> u32 {
    static POWERS_OF_10: [u64; 20] = [
        0,
        10,
        100,
        1000,
        10000,
        100000,
        1000000,
        10000000,
        100000000,
        1000000000,
        10000000000,
        100000000000,
        1000000000000,
        10000000000000,
        100000000000000,
        1000000000000000,
        10000000000000000,
        100000000000000000,
        1000000000000000000,
        10000000000000000000,
    ];

    let t = ((64 - (n | 1).leading_zeros()) * 1233) >> 12;
    t - u32::from(n < *unsafe { POWERS_OF_10.get_unchecked(t as usize) }) + 1
}
